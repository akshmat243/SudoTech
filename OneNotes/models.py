from django.db import models

class OneNoteNotebook(models.Model):
    title = models.CharField(max_length=255)
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    def __str__(self):
        return self.title


class OneNoteSection(models.Model):
    notebook = models.ForeignKey(OneNoteNotebook, related_name='sections', on_delete=models.CASCADE)
    title = models.CharField(max_length=255)
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    def __str__(self):
        return f"{self.title} - {self.notebook.title}"


class OneNotePage(models.Model):
    section = models.ForeignKey(OneNoteSection, related_name='pages', on_delete=models.CASCADE)
    title = models.CharField(max_length=255)
    content = models.TextField()
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    def __str__(self):
        return f"Page: {self.title} in Section: {self.section.title}"
